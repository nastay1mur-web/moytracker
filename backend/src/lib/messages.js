function reminderMessage(time, habits) {
    // habits = [{ name, emoji, streak }]
    if (habits.length === 1) {
        const h = habits[0];
        const streakText = h.streak > 0
            ? `Серия: 🔥 ${h.streak} ${declDays(h.streak)} — не прерывай!`
            : `Начни новую серию сегодня!`;
        return `⏰ ${time}\n\n${h.emoji} Пора: <b>${h.name}</b>\n${streakText}`;
    }

    const list = habits.map(h => {
        const s = h.streak > 0 ? ` (🔥${h.streak})` : '';
        return `${h.emoji} ${h.name}${s}`;
    }).join('\n');

    return `⏰ ${time}\n\nПора сделать:\n${list}`;
}

function weeklyReport(firstName, stats) {
    const pct     = stats.completion_pct;
    const total   = stats.total_completions;
    const emoji   = pct >= 80 ? '🏆' : pct >= 50 ? '💪' : '💡';
    const comment = pct >= 80
        ? 'Отличная неделя!'
        : pct >= 50
        ? 'Хорошее начало, продолжай!'
        : 'Есть куда расти — главное не останавливаться';

    const topHabits = [...stats.habits]
        .sort((a, b) => b.streak - a.streak)
        .slice(0, 3)
        .map(h => `  ${h.emoji} ${h.name} — 🔥${h.streak}`)
        .join('\n');

    return `${emoji} <b>${firstName}, итоги недели:</b>

Выполнено: <b>${pct}%</b> (${total} раз)
${comment}

<b>Топ серий:</b>
${topHabits || '  Пока нет данных'}`;
}

function achievementMessage(habitEmoji, habitName, type) {
    const texts = {
        streak_7:   `🎯 Серия 7 дней подряд!\nПервая неделя пройдена — это настоящий старт!`,
        streak_30:  `🏆 Серия 30 дней подряд!\nМесяц без пропуска — привычка закрепляется навсегда!`,
        streak_100: `👑 Серия 100 дней подряд!\nЭто уже образ жизни. Ты можешь всё!`,
    };
    return `${texts[type] || '🎉 Новое достижение!'}\n\nПривычка: ${habitEmoji} ${habitName}`;
}

function subscriptionExpiringSoon(firstName, daysLeft) {
    return `⚠️ ${firstName}, подписка истекает через ${daysLeft} ${declDays(daysLeft)}.

После окончания будут доступны только 3 привычки.
Все данные и история сохранятся.`;
}

function subscriptionExpired(firstName) {
    return `${firstName}, подписка закончилась.

Твои данные и история в сохранности.
Доступны первые 3 привычки из списка.`;
}

// Склонение слова "день"
function declDays(n) {
    const abs = Math.abs(n) % 100;
    const mod = abs % 10;
    if (abs > 10 && abs < 20) return 'дней';
    if (mod === 1) return 'день';
    if (mod >= 2 && mod <= 4) return 'дня';
    return 'дней';
}

